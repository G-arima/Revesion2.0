package basicprogramming;

public class LiteralProgram {

	public static void main(String[] args) {
		String name="Garima";
		int age=31;
		int salary=95000;
		//Here Garima,31 and 95000 are literals means any sort of values given in the program.
		System.out.println("The value of the name is : " +name);
		System.out.println("The value of the age is : "+age);
		System.out.println("The value of the salary is : "+salary);

	}

}
